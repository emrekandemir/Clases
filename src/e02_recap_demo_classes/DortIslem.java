package e02_recap_demo_classes;

public class DortIslem {

    //ortak operasyonlar(metotlar)
    public int topla(int num1, int num2){
        return num1 + num2;
    }
    public int cikar(int num1, int num2){
        return num1 - num2;
    }
    public int carp(int num1, int num2){
        return num1 * num2;
    }
    public int bol(int num1, int num2){
        return num1 / num2;
    }
}

//Java'da return ifadesi, metotlardan değer veya nesne döndürmek için kullanılır.
// Döndürülen değerin türü, metodun geri dönüş tipi ile uyumlu olmalıdır.